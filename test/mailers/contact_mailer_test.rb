require 'test_helper'

class ContactMailerTest < ActionMailer::TestCase
  test "name" do
    mail = ContactMailer.name
    assert_equal "Name", mail.subject
    assert_equal ["to@example.org"], mail.to
    assert_equal ["from@example.com"], mail.from
    assert_match "Hi", mail.body.encoded
  end

  test "email" do
    mail = ContactMailer.email
    assert_equal "Email", mail.subject
    assert_equal ["to@example.org"], mail.to
    assert_equal ["from@example.com"], mail.from
    assert_match "Hi", mail.body.encoded
  end

  test "phone" do
    mail = ContactMailer.phone
    assert_equal "Phone", mail.subject
    assert_equal ["to@example.org"], mail.to
    assert_equal ["from@example.com"], mail.from
    assert_match "Hi", mail.body.encoded
  end

  test "message:text" do
    mail = ContactMailer.message:text
    assert_equal "Message:text", mail.subject
    assert_equal ["to@example.org"], mail.to
    assert_equal ["from@example.com"], mail.from
    assert_match "Hi", mail.body.encoded
  end

end
